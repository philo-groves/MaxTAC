def note_payload_from_args(root: Path, args: argparse.Namespace, body: str, *, existing: dict[str, Any] | None = None) -> dict[str, Any]:
    timestamp = now()
    doc_id = args.doc_id or (existing or {}).get("doc_id") or next_id(root, "corpus_documents", "doc_id", DOC_PREFIX)
    body_path = f"research/notes/{doc_id}.md"
    payload = dict(existing or {})
    payload.update(
        {
            "doc_id": doc_id,
            "title": args.title if args.title is not None else payload.get("title", ""),
            "kind": args.kind if args.kind is not None else payload.get("kind", "reference"),
            "status": args.status if args.status is not None else payload.get("status", "draft"),
            "summary": args.summary if args.summary is not None else payload.get("summary", ""),
            "body_path": body_path,
            "updated_at": timestamp,
            "evidence": parse_repeated(args.evidence) if args.evidence is not None else payload.get("evidence", []),
            "model_refs": parse_repeated(args.model_ref) if args.model_ref is not None else payload.get("model_refs", []),
        }
    )
    payload.setdefault("created_at", timestamp)
    if args.tag is not None:
        payload["tags"] = parse_tags(args.tag)
    else:
        payload.setdefault("tags", [])
    payload["line_count"] = count_lines(body)
    payload["word_count"] = count_words(body)
    return payload


def body_from_args(args: argparse.Namespace) -> str:
    if args.body_file:
        return read_text_file(Path(args.body_file).expanduser())
    if args.body:
        return args.body
    if not sys.stdin.isatty():
        data = sys.stdin.read()
        if data.strip():
            return data
    raise SystemExit("body is required via --body, --body-file, or stdin")


def cmd_add_note(args: argparse.Namespace) -> None:
    root = root_path(args.root)
    ensure_dirs(root)
    body = body_from_args(args)
    payload = note_payload_from_args(root, args, body)
    errors, warnings = validate_doc_payload(payload, body, strict=not args.allow_thin)
    for warning in warnings:
        print(f"warning: {warning}", file=sys.stderr)
    if errors:
        raise SystemExit("\n".join(errors))
    if document_exists(root, payload["doc_id"]):
        raise SystemExit(f"corpus document already exists: {payload['doc_id']}; use update-note to modify it")
    note_path = workspace_path(root, payload["body_path"], "note path")
    write_text_file(note_path, render_note_markdown(payload, body))
    upsert_document(root, payload, body, allow_update=False)
    print(payload["doc_id"])


def cmd_update_note(args: argparse.Namespace) -> None:
    root = root_path(args.root)
    ensure_dirs(root)
    existing = get_document(root, args.doc_id)
    current_body = document_body(root, existing)
    if args.append:
        body = current_body.rstrip() + "\n\n" + args.append.strip()
    elif args.body_file or args.body or not sys.stdin.isatty():
        body = body_from_args(args)
    else:
        body = current_body
    payload = note_payload_from_args(root, args, body, existing=existing)
    if args.add_tag:
        existing_tags = {tag["key"]: tag for tag in payload.get("tags", [])}
        for tag in parse_tags(args.add_tag):
            existing_tags[tag["key"]] = tag
        payload["tags"] = list(existing_tags.values())
    errors, warnings = validate_doc_payload(payload, body, strict=not args.allow_thin)
    for warning in warnings:
        print(f"warning: {warning}", file=sys.stderr)
    if errors:
        raise SystemExit("\n".join(errors))
    note_path = workspace_path(root, payload["body_path"], "note path")
    write_text_file(note_path, render_note_markdown(payload, body))
    upsert_document(root, payload, body, allow_update=True)
    print(payload["doc_id"])


def cmd_import_file(args: argparse.Namespace) -> None:
    root = root_path(args.root)
    source = workspace_path(root, args.markdown_file, "markdown file")
    text = read_text_file(source)
    body = body_from_note_markdown(text)
    title = args.title
    if not title:
        match = re.search(r"^#\s+(.+?)\s*$", text, re.MULTILINE)
        title = match.group(1).strip() if match else source.stem.replace("-", " ").title()
    summary = args.summary or body.splitlines()[0][:240] if body.splitlines() else f"Imported from {relative_to_root(root, source)}."
    import_args = argparse.Namespace(
        root=args.root,
        doc_id=args.doc_id,
        title=title,
        kind=args.kind,
        status=args.status,
        summary=summary,
        tag=args.tag,
        evidence=[relative_to_root(root, source), *(args.evidence or [])],
        model_ref=args.model_ref,
    )
    payload = note_payload_from_args(root, import_args, body)
    errors, warnings = validate_doc_payload(payload, body, strict=not args.allow_thin)
    for warning in warnings:
        print(f"warning: {warning}", file=sys.stderr)
    if errors:
        raise SystemExit("\n".join(errors))
    if document_exists(root, payload["doc_id"]):
        raise SystemExit(f"corpus document already exists: {payload['doc_id']}; use update-note to modify it")
    note_path = workspace_path(root, payload["body_path"], "note path")
    write_text_file(note_path, render_note_markdown(payload, body))
    upsert_document(root, payload, body, allow_update=False)
    if args.move:
        archive_dir = corpus_dirs(root)["artifacts"] / "imported-markdown"
        archive_dir.mkdir(parents=True, exist_ok=True)
        destination = archive_dir / source.name
        if destination.exists():
            destination = archive_dir / f"{source.stem}-{payload['doc_id']}{source.suffix}"
        shutil.move(str(source), destination)
        print(f"moved source to {relative_to_root(root, destination)}")
    print(payload["doc_id"])


