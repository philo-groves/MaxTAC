def cmd_lint(args: argparse.Namespace) -> None:
    root = root_path(args.root)
    docs = list_documents(root)
    errors: list[str] = []
    warnings: list[str] = []
    for doc in docs:
        body = document_body(root, doc)
        doc_errors, doc_warnings = validate_doc_payload(doc, body, strict=True)
        errors.extend(f"{doc['doc_id']}: {error}" for error in doc_errors)
        warnings.extend(f"{doc['doc_id']}: {warning}" for warning in doc_warnings)
    legacy = legacy_markdown(root)
    if legacy:
        warnings.append(
            f"{len(legacy)} markdown file(s) live outside research/notes, research/views, or research/artifacts"
        )
    for warning in warnings:
        print(f"warning: {warning}")
    for error in errors:
        print(f"error: {error}")
    if errors or (args.strict and warnings):
        raise SystemExit(1)
    print(f"Corpus lint ok: {len(docs)} note(s)")


def legacy_markdown(root: Path) -> list[Path]:
    research = root / "research"
    if not research.exists():
        return []
    allowed_roots = {"notes", "views", "artifacts"}
    result: list[Path] = []
    for path in sorted(research.rglob("*.md")):
        try:
            parts = path.relative_to(research).parts
        except ValueError:
            continue
        if not parts:
            continue
        if parts[0] not in allowed_roots:
            result.append(path)
    return result


