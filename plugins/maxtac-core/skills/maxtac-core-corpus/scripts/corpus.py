def now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def root_path(value: str | None) -> Path:
    root = Path(value or ".").expanduser().resolve()
    root.mkdir(parents=True, exist_ok=True)
    return root


def relative_to_root(root: Path, path: Path) -> str:
    try:
        return path.resolve().relative_to(root.resolve()).as_posix()
    except ValueError:
        return str(path)


def workspace_path(root: Path, value: str | Path, label: str = "path") -> Path:
    path = Path(value).expanduser()
    if not path.is_absolute():
        path = root / path
    resolved_root = root.resolve()
    resolved_path = path.resolve()
    try:
        resolved_path.relative_to(resolved_root)
    except ValueError as exc:
        raise SystemExit(f"{label} escapes workspace root: {resolved_path}") from exc
    return resolved_path


def corpus_dirs(root: Path) -> dict[str, Path]:
    research = root / "research"
    return {
        "research": research,
        "notes": research / "notes",
        "views": research / "views",
        "artifacts": research / "artifacts",
    }


def ensure_dirs(root: Path) -> None:
    for path in corpus_dirs(root).values():
        path.mkdir(parents=True, exist_ok=True)


def read_text_file(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8", errors="replace")
    except FileNotFoundError as exc:
        raise SystemExit(f"missing text file: {path}") from exc


def write_text_file(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.rstrip() + "\n", encoding="utf-8")


def parse_tag(value: str) -> dict[str, Any]:
    raw = value.strip()
    weight = 1.0
    source = "manual"
    if not raw:
        raise SystemExit("empty tag")
    if "@" in raw:
        raw, raw_weight = raw.rsplit("@", 1)
        try:
            weight = float(raw_weight)
        except ValueError as exc:
            raise SystemExit(f"invalid tag weight: {value}") from exc
    if "=" in raw:
        facet, tag_value = raw.split("=", 1)
    elif ":" in raw:
        facet, tag_value = raw.split(":", 1)
    else:
        raise SystemExit(f"tag must use facet=value: {value}")
    facet = slug_part(facet, "facet")
    tag_value = slug_part(tag_value, "value")
    if not facet or not tag_value:
        raise SystemExit(f"invalid tag: {value}")
    return {
        "facet": facet,
        "value": tag_value,
        "key": tag_key(facet, tag_value),
        "weight": weight,
        "source": source,
    }


def parse_tags(values: list[str] | None) -> list[dict[str, Any]]:
    tags: list[dict[str, Any]] = []
    seen: set[str] = set()
    for value in values or []:
        for part in re.split(r"[,;\n]+", value):
            if not part.strip():
                continue
            tag = parse_tag(part)
            if tag["key"] not in seen:
                tags.append(tag)
                seen.add(tag["key"])
    return tags


def tag_key(facet: str, value: str) -> str:
    return f"{facet}:{value}"


def slug_part(value: str, default: str) -> str:
    cleaned = re.sub(r"[^a-zA-Z0-9._-]+", "-", value.strip().lower()).strip("-._")
    return cleaned or default


def parse_repeated(values: list[str] | None) -> list[str]:
    result: list[str] = []
    for value in values or []:
        for part in re.split(r"[,;\n]+", value):
            stripped = part.strip()
            if stripped and stripped not in result:
                result.append(stripped)
    return result


def token_set(*values: Any) -> set[str]:
    text = " ".join(str(value or "") for value in values)
    return {part for part in re.split(r"[^a-zA-Z0-9_+-]+", text.lower()) if len(part) > 2}


def fts_query(text: str) -> str:
    parts = sorted(token_set(text))
    return " OR ".join(f'"{part.replace(chr(34), chr(34) + chr(34))}"' for part in parts)


def read_json_value(path: Path) -> dict[str, Any]:
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except FileNotFoundError as exc:
        raise SystemExit(f"missing JSON file: {path}") from exc
    except json.JSONDecodeError as exc:
        raise SystemExit(f"{path}: invalid JSON: {exc}") from exc
    if not isinstance(payload, dict):
        raise SystemExit(f"{path}: expected a JSON object")
    return payload


def validate_identifier(value: str, label: str) -> None:
    if not IDENTIFIER_RE.fullmatch(value):
        raise SystemExit(f"invalid SQL {label}: {value}")


def next_id(root: Path, table: str, column: str, prefix: str) -> str:
    validate_identifier(table, "table")
    validate_identifier(column, "column")
    workspace_db.initialize_workspace_db(root)
    pattern = re.compile(rf"{re.escape(prefix)}-(\d{{6}})$")
    scope = f"{table}.{column}"
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        conn.execute("BEGIN IMMEDIATE")
        highest = 0
        rows = conn.execute(f"SELECT {column} FROM {table}").fetchall()
        for row in rows:
            value = str(row[column])
            match = pattern.fullmatch(value)
            if match:
                highest = max(highest, int(match.group(1)))
        counter = conn.execute("SELECT next_value FROM id_counters WHERE scope = ?", (scope,)).fetchone()
        next_value = max(highest + 1, int(counter["next_value"]) if counter else 1)
        conn.execute(
            """
            INSERT INTO id_counters(scope, next_value, updated_at)
            VALUES (?, ?, ?)
            ON CONFLICT(scope) DO UPDATE SET
              next_value=excluded.next_value,
              updated_at=excluded.updated_at
            """,
            (scope, next_value + 1, now()),
        )
        conn.commit()
    return f"{prefix}-{next_value:06d}"


def count_lines(text: str) -> int:
    if not text:
        return 0
    return len(text.splitlines())


def count_words(text: str) -> int:
    return len(re.findall(r"\b\S+\b", text))


def validate_doc_payload(payload: dict[str, Any], body: str, *, strict: bool = True) -> tuple[list[str], list[str]]:
    errors: list[str] = []
    warnings: list[str] = []
    for key in ("doc_id", "title", "kind", "status", "summary", "body_path", "created_at", "updated_at"):
        if not str(payload.get(key, "")).strip():
            errors.append(f"{key} is required")
    if payload.get("kind") not in DOC_KINDS:
        errors.append(f"kind must be one of {', '.join(DOC_KINDS)}")
    if payload.get("status") not in DOC_STATUSES:
        errors.append(f"status must be one of {', '.join(DOC_STATUSES)}")
    tags = payload.get("tags")
    if not isinstance(tags, list):
        errors.append("tags must be an array")
        tags = []
    else:
        tag_keys = [str(tag.get("key", "")) for tag in tags if isinstance(tag, dict)]
        if len(tag_keys) != len(set(tag_keys)):
            errors.append("tags contain duplicates")
        if strict and len(tags) < 2:
            errors.append("at least two tags are required; use --allow-thin only for temporary drafts")
        facets = {str(tag.get("facet", "")) for tag in tags if isinstance(tag, dict)}
        if strict and not facets.intersection({"subsystem", "boundary", "asset", "knowledge-kind"}):
            errors.append("at least one subsystem, boundary, asset, or knowledge-kind tag is required")
        if "knowledge-kind" not in facets:
            warnings.append("missing knowledge-kind tag")
    evidence = payload.get("evidence")
    if not isinstance(evidence, list):
        errors.append("evidence must be an array")
        evidence = []
    if payload.get("status") in EVIDENCE_REQUIRED_STATUSES and not evidence:
        errors.append(f"evidence is required when status is {payload.get('status')}")
    if not str(payload.get("summary", "")).strip():
        errors.append("summary is required")
    if strict and len(str(payload.get("summary", ""))) < 20:
        warnings.append("summary is very short")
    if not body.strip():
        errors.append("body is required")
    if count_lines(body) > 220:
        warnings.append("body is over 220 lines; split the note or use artifact storage for raw output")
    if count_lines(body) > 320:
        errors.append("body is over 320 lines; corpus notes must stay compact")
    return errors, warnings


def validate_edge_payload(payload: dict[str, Any]) -> tuple[list[str], list[str]]:
    errors: list[str] = []
    warnings: list[str] = []
    for key in ("edge_id", "src_doc_id", "predicate", "dst_doc_id", "status", "confidence", "created_at", "updated_at"):
        if not str(payload.get(key, "")).strip():
            errors.append(f"{key} is required")
    if payload.get("status") not in EDGE_STATUSES:
        errors.append(f"status must be one of {', '.join(EDGE_STATUSES)}")
    if payload.get("confidence") not in CONFIDENCES:
        errors.append(f"confidence must be one of {', '.join(CONFIDENCES)}")
    if payload.get("status") in EDGE_EVIDENCE_REQUIRED_STATUSES and not str(payload.get("evidence", "")).strip():
        errors.append(f"evidence is required when edge status is {payload.get('status')}")
    if payload.get("src_doc_id") == payload.get("dst_doc_id"):
        warnings.append("edge links a document to itself")
    return errors, warnings


def render_note_markdown(payload: dict[str, Any], body: str) -> str:
    tags = ", ".join(f"{tag['facet']}={tag['value']}" for tag in payload.get("tags", []))
    evidence = payload.get("evidence", [])
    model_refs = payload.get("model_refs", [])
    lines = [
        f"# {payload['title']}",
        "",
        "<!-- MaxTAC corpus note. Use maxtac-core-corpus/scripts/corpus.py to edit indexed metadata. -->",
        "",
        "## Metadata",
        "",
        f"- ID: `{payload['doc_id']}`",
        f"- Kind: `{payload['kind']}`",
        f"- Status: `{payload['status']}`",
        f"- Tags: {tags}",
        f"- Updated: `{payload['updated_at']}`",
        "",
        "## Summary",
        "",
        payload["summary"].strip(),
        "",
        "## Evidence",
        "",
    ]
    if evidence:
        for item in evidence:
            lines.append(f"- `{item}`")
    else:
        lines.append("- None recorded.")
    lines.extend(["", "## Model References", ""])
    if model_refs:
        for item in model_refs:
            lines.append(f"- `{item}`")
    else:
        lines.append("- None recorded.")
    lines.extend(["", "## Body", "", body.rstrip(), ""])
    return "\n".join(lines)


def body_from_note_markdown(text: str) -> str:
    match = re.search(r"^## Body\s*$", text, re.MULTILINE)
    if not match:
        return text.strip()
    return text[match.end() :].strip()


def upsert_tag(conn: Any, tag: dict[str, Any]) -> None:
    conn.execute(
        """
        INSERT INTO corpus_tags(tag_key, facet, value)
        VALUES (?, ?, ?)
        ON CONFLICT(tag_key) DO UPDATE SET facet=excluded.facet, value=excluded.value
        """,
        (tag["key"], tag["facet"], tag["value"]),
    )


def document_exists(root: Path, doc_id: str) -> bool:
    workspace_db.initialize_workspace_db(root)
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        row = conn.execute("SELECT 1 FROM corpus_documents WHERE doc_id = ?", (doc_id,)).fetchone()
    return bool(row)


def upsert_document(root: Path, payload: dict[str, Any], body: str, *, allow_update: bool) -> None:
    workspace_db.initialize_workspace_db(root)
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        doc_id = payload["doc_id"]
        exists = conn.execute("SELECT 1 FROM corpus_documents WHERE doc_id = ?", (doc_id,)).fetchone()
        if exists and not allow_update:
            raise SystemExit(f"corpus document already exists: {doc_id}; use update-note to modify it")
        if allow_update and workspace_db.fts5_enabled(conn):
            conn.execute("DELETE FROM corpus_search WHERE doc_id = ?", (doc_id,))
        if allow_update:
            conn.execute("DELETE FROM corpus_document_tags WHERE doc_id = ?", (doc_id,))
        payload["line_count"] = count_lines(body)
        payload["word_count"] = count_words(body)
        raw_json = json.dumps(payload, sort_keys=True)
        values = (
            doc_id,
            payload["title"],
            payload["kind"],
            payload["status"],
            payload["summary"],
            payload["body_path"],
            payload["line_count"],
            payload["word_count"],
            payload["created_at"],
            payload["updated_at"],
            raw_json,
        )
        if allow_update:
            conn.execute(
                """
                INSERT INTO corpus_documents(
                  doc_id, title, kind, status, summary, body_path, line_count, word_count,
                  created_at, updated_at, raw_json
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                ON CONFLICT(doc_id) DO UPDATE SET
                  title=excluded.title,
                  kind=excluded.kind,
                  status=excluded.status,
                  summary=excluded.summary,
                  body_path=excluded.body_path,
                  line_count=excluded.line_count,
                  word_count=excluded.word_count,
                  updated_at=excluded.updated_at,
                  raw_json=excluded.raw_json
                """,
                values,
            )
        else:
            try:
                conn.execute(
                    """
                    INSERT INTO corpus_documents(
                      doc_id, title, kind, status, summary, body_path, line_count, word_count,
                      created_at, updated_at, raw_json
                    ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    values,
                )
            except sqlite3.IntegrityError as exc:
                raise SystemExit(f"corpus document already exists: {doc_id}; use update-note to modify it") from exc
        tags_text_parts: list[str] = []
        for tag in payload.get("tags", []):
            upsert_tag(conn, tag)
            conn.execute(
                """
                INSERT INTO corpus_document_tags(doc_id, tag_key, weight, source)
                VALUES (?, ?, ?, ?)
                ON CONFLICT(doc_id, tag_key) DO UPDATE SET
                  weight=excluded.weight,
                  source=excluded.source
                """,
                (doc_id, tag["key"], float(tag.get("weight", 1.0)), str(tag.get("source", "manual"))),
            )
            tags_text_parts.extend([tag["facet"], tag["value"], tag["key"]])
        if workspace_db.fts5_enabled(conn):
            conn.execute(
                "INSERT INTO corpus_search(doc_id, title, summary, body, tags) VALUES (?, ?, ?, ?, ?)",
                (doc_id, payload["title"], payload["summary"], body, " ".join(tags_text_parts)),
            )
        conn.commit()


def get_document(root: Path, doc_id: str) -> dict[str, Any]:
    workspace_db.initialize_workspace_db(root)
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        row = conn.execute("SELECT raw_json FROM corpus_documents WHERE doc_id = ?", (doc_id,)).fetchone()
    if not row:
        raise SystemExit(f"corpus document not found: {doc_id}")
    return json.loads(row["raw_json"])


def document_body(root: Path, payload: dict[str, Any]) -> str:
    path = workspace_path(root, payload["body_path"], "body path")
    return body_from_note_markdown(read_text_file(path))


def list_documents(root: Path, *, tag_filters: list[dict[str, Any]] | None = None, kind: str | None = None, status: str | None = None) -> list[dict[str, Any]]:
    workspace_db.initialize_workspace_db(root)
    clauses = ["1=1"]
    params: list[Any] = []
    if kind:
        clauses.append("kind = ?")
        params.append(kind)
    if status:
        clauses.append("status = ?")
        params.append(status)
    for tag in tag_filters or []:
        clauses.append(
            "EXISTS (SELECT 1 FROM corpus_document_tags cdt WHERE cdt.doc_id = corpus_documents.doc_id AND cdt.tag_key = ?)"
        )
        params.append(tag["key"])
    sql = "SELECT raw_json FROM corpus_documents WHERE " + " AND ".join(clauses) + " ORDER BY updated_at DESC, doc_id"
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        rows = conn.execute(sql, params).fetchall()
    return [json.loads(row["raw_json"]) for row in rows]


def search_documents(root: Path, query: str, *, tag_filters: list[dict[str, Any]], kind: str | None, status: str | None, limit: int) -> list[tuple[float, dict[str, Any]]]:
    workspace_db.initialize_workspace_db(root)
    tag_filters = tag_filters or []
    limit = max(1, limit)
    query_text = fts_query(query)
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        clauses = []
        params: list[Any] = []
        if kind:
            clauses.append("corpus_documents.kind = ?")
            params.append(kind)
        if status:
            clauses.append("corpus_documents.status = ?")
            params.append(status)
        for tag in tag_filters:
            clauses.append(
                "EXISTS (SELECT 1 FROM corpus_document_tags cdt WHERE cdt.doc_id = corpus_documents.doc_id AND cdt.tag_key = ?)"
            )
            params.append(tag["key"])
        where_filters = " AND ".join(clauses)
        if workspace_db.fts5_enabled(conn) and query_text:
            sql = (
                "SELECT corpus_documents.raw_json AS raw_json, bm25(corpus_search) AS rank "
                "FROM corpus_search JOIN corpus_documents USING(doc_id) "
                "WHERE corpus_search MATCH ?"
            )
            query_params: list[Any] = [query_text]
            if where_filters:
                sql += f" AND {where_filters}"
                query_params.extend(params)
            sql += " ORDER BY rank ASC LIMIT ?"
            query_params.append(limit)
            rows = conn.execute(sql, query_params).fetchall()
            return [(round(-float(row["rank"]), 6), json.loads(row["raw_json"])) for row in rows]
        docs = list_documents(root, tag_filters=tag_filters, kind=kind, status=status)
    wanted = token_set(query)
    scored: list[tuple[float, dict[str, Any]]] = []
    for doc in docs:
        body = document_body(root, doc)
        haystack = " ".join(
            [
                doc.get("doc_id", ""),
                doc.get("title", ""),
                doc.get("kind", ""),
                doc.get("status", ""),
                doc.get("summary", ""),
                body,
                " ".join(tag["key"] for tag in doc.get("tags", [])),
            ]
        )
        overlap = wanted & token_set(haystack)
        if overlap or not wanted:
            scored.append((float(len(overlap)), doc))
    scored.sort(key=lambda item: (item[0], item[1].get("updated_at", "")), reverse=True)
    return scored[:limit]


def tag_counts(root: Path) -> list[dict[str, Any]]:
    workspace_db.initialize_workspace_db(root)
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        rows = conn.execute(
            """
            SELECT corpus_tags.facet, corpus_tags.value, corpus_tags.tag_key, COUNT(corpus_document_tags.doc_id) AS count
            FROM corpus_tags
            LEFT JOIN corpus_document_tags USING(tag_key)
            GROUP BY corpus_tags.tag_key
            ORDER BY corpus_tags.facet, count DESC, corpus_tags.value
            """
        ).fetchall()
    return [
        {"facet": str(row["facet"]), "value": str(row["value"]), "key": str(row["tag_key"]), "count": int(row["count"])}
        for row in rows
    ]


def upsert_edge(root: Path, payload: dict[str, Any], *, allow_update: bool) -> None:
    errors, warnings = validate_edge_payload(payload)
    for warning in warnings:
        print(f"warning: {warning}", file=sys.stderr)
    if errors:
        raise SystemExit("\n".join(errors))
    workspace_db.initialize_workspace_db(root)
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        for doc_id in (payload["src_doc_id"], payload["dst_doc_id"]):
            row = conn.execute("SELECT doc_id FROM corpus_documents WHERE doc_id = ?", (doc_id,)).fetchone()
            if not row:
                raise SystemExit(f"edge references unknown document: {doc_id}")
        values = (
            payload["edge_id"],
            payload["src_doc_id"],
            payload["predicate"],
            payload["dst_doc_id"],
            payload["status"],
            payload["confidence"],
            payload.get("evidence", ""),
            payload.get("note", ""),
            payload["created_at"],
            payload["updated_at"],
            json.dumps(payload, sort_keys=True),
        )
        if allow_update:
            conn.execute(
                """
                INSERT INTO corpus_edges(
                  edge_id, src_doc_id, predicate, dst_doc_id, status, confidence,
                  evidence, note, created_at, updated_at, raw_json
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                ON CONFLICT(edge_id) DO UPDATE SET
                  src_doc_id=excluded.src_doc_id,
                  predicate=excluded.predicate,
                  dst_doc_id=excluded.dst_doc_id,
                  status=excluded.status,
                  confidence=excluded.confidence,
                  evidence=excluded.evidence,
                  note=excluded.note,
                  updated_at=excluded.updated_at,
                  raw_json=excluded.raw_json
                """,
                values,
            )
        else:
            try:
                conn.execute(
                    """
                    INSERT INTO corpus_edges(
                      edge_id, src_doc_id, predicate, dst_doc_id, status, confidence,
                      evidence, note, created_at, updated_at, raw_json
                    ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    values,
                )
            except sqlite3.IntegrityError as exc:
                raise SystemExit(f"corpus edge already exists: {payload['edge_id']}; pass --edge-id to update it") from exc
        conn.commit()


def list_edges(root: Path, doc_id: str | None = None) -> list[dict[str, Any]]:
    workspace_db.initialize_workspace_db(root)
    params: list[Any] = []
    where = ""
    if doc_id:
        where = "WHERE src_doc_id = ? OR dst_doc_id = ?"
        params.extend([doc_id, doc_id])
    with workspace_db.connect(root) as conn:
        workspace_db.ensure_schema(conn)
        rows = conn.execute(f"SELECT raw_json FROM corpus_edges {where} ORDER BY updated_at DESC, edge_id", params).fetchall()
    return [json.loads(row["raw_json"]) for row in rows]


def graph_neighbors(root: Path, doc_ids: list[str], limit: int) -> list[dict[str, Any]]:
    if not doc_ids:
        return []
    seen_docs = set(doc_ids)
    neighbors: list[dict[str, Any]] = []
    for edge in list_edges(root):
        other = None
        if edge["src_doc_id"] in seen_docs:
            other = edge["dst_doc_id"]
        elif edge["dst_doc_id"] in seen_docs:
            other = edge["src_doc_id"]
        if other and other not in seen_docs:
            try:
                doc = get_document(root, other)
            except SystemExit:
                continue
            neighbors.append({"edge": edge, "document": doc})
            seen_docs.add(other)
        if len(neighbors) >= limit:
            break
    return neighbors


def diversify_results(results: list[tuple[float, dict[str, Any]]], facets: list[str], limit: int) -> list[tuple[float, dict[str, Any]]]:
    if not facets:
        return results[:limit]
    selected: list[tuple[float, dict[str, Any]]] = []
    selected_ids: set[str] = set()
    buckets: dict[str, int] = defaultdict(int)
    per_bucket_soft_cap = max(1, limit // max(2, min(limit, len(facets) * 2)))

    def bucket_for(doc: dict[str, Any]) -> str:
        tags = doc.get("tags", [])
        values = []
        for facet in facets:
            facet_values = sorted(tag["value"] for tag in tags if tag.get("facet") == facet)
            if facet_values:
                values.append(f"{facet}={facet_values[0]}")
        return "|".join(values) or "untagged"

    for score, doc in results:
        bucket = bucket_for(doc)
        if buckets[bucket] < per_bucket_soft_cap:
            selected.append((score, doc))
            selected_ids.add(doc["doc_id"])
            buckets[bucket] += 1
        if len(selected) >= limit:
            return selected
    for score, doc in results:
        if doc["doc_id"] not in selected_ids:
            selected.append((score, doc))
            if len(selected) >= limit:
                break
    return selected


def format_doc_line(doc: dict[str, Any], score: float | None = None) -> str:
    score_text = f" score={score}" if score is not None else ""
    tags = ", ".join(tag["key"] for tag in doc.get("tags", [])[:6])
    return f"- `{doc['doc_id']}`{score_text} `{doc['kind']}` `{doc['status']}` {doc['title']} [{tags}]"


def render_context_pack(root: Path, query: str, results: list[tuple[float, dict[str, Any]]], *, facets: list[str], limit: int) -> str:
    selected = diversify_results(results, facets, limit)
    selected_docs = [doc for _, doc in selected]
    selected_ids = [doc["doc_id"] for doc in selected_docs]
    neighbors = graph_neighbors(root, selected_ids, max(3, limit // 2))
    tag_counter: Counter[str] = Counter()
    for doc in selected_docs:
        for tag in doc.get("tags", []):
            tag_counter[tag["key"]] += 1
    all_counts = tag_counts(root)
    underexplored = [
        item for item in all_counts if item["count"] > 0 and item["key"] not in tag_counter
    ][:10]
    unknowns = [
        doc for doc in list_documents(root, kind="open-question") if doc["doc_id"] not in selected_ids
    ][:5]
    negative = [
        doc for doc in list_documents(root, kind="negative-result") if doc["doc_id"] not in selected_ids
    ][:5]
    lines = [
        "# MaxTAC Corpus Orientation Pack",
        "",
        f"- Query: {query}",
        f"- Diversify by: {', '.join(facets) if facets else 'none'}",
        "",
        "## Primary Notes",
        "",
    ]
    if not selected:
        lines.append("No matching corpus notes.")
    for score, doc in selected:
        lines.append(format_doc_line(doc, score))
        lines.append(f"  Summary: {doc['summary']}")
    lines.extend(["", "## Graph Neighbors", ""])
    if not neighbors:
        lines.append("No graph neighbors recorded.")
    for item in neighbors:
        edge = item["edge"]
        doc = item["document"]
        lines.append(
            f"- `{doc['doc_id']}` via `{edge['predicate']}` `{edge['src_doc_id']}` -> `{edge['dst_doc_id']}`: {doc['title']}"
        )
    lines.extend(["", "## Negative Knowledge", ""])
    if not negative:
        lines.append("No adjacent negative-result notes found.")
    for doc in negative:
        lines.append(format_doc_line(doc))
        lines.append(f"  Summary: {doc['summary']}")
    lines.extend(["", "## Open Questions", ""])
    if not unknowns:
        lines.append("No open-question notes found.")
    for doc in unknowns:
        lines.append(format_doc_line(doc))
        lines.append(f"  Summary: {doc['summary']}")
    lines.extend(["", "## Underexplored Tags", ""])
    if not underexplored:
        lines.append("No underexplored tags found.")
    for item in underexplored:
        lines.append(f"- `{item['key']}` ({item['count']} note(s))")
    lines.extend(
        [
            "",
            "## Guidance",
            "",
            "- Read primary notes before adding new corpus content.",
            "- Prefer updating or linking an existing note when the new fact refines, contradicts, or supersedes it.",
            "- If continuing in one subsystem, name the reason and checkpoint; otherwise pivot to an adjacent underexplored tag or open question.",
            "",
        ]
    )
    return "\n".join(lines)


def render_views(root: Path) -> dict[str, str]:
    docs = list_documents(root)
    edges = list_edges(root)
    counts = tag_counts(root)
    by_kind: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for doc in docs:
        by_kind[doc["kind"]].append(doc)
    index_lines = [
        GENERATED_NOTICE,
        "# Research Corpus Index",
        "",
        f"- Documents: {len(docs)}",
        f"- Edges: {len(edges)}",
        "",
        "## Recent Notes",
        "",
    ]
    for doc in docs[:25]:
        index_lines.append(format_doc_line(doc))
        index_lines.append(f"  Summary: {doc['summary']}")
    index_lines.extend(["", "## By Kind", ""])
    for kind in sorted(by_kind):
        index_lines.append(f"### {kind}")
        index_lines.append("")
        for doc in by_kind[kind]:
            index_lines.append(format_doc_line(doc))
        index_lines.append("")

    facet_lines = [GENERATED_NOTICE, "# Corpus Facets", ""]
    current_facet = None
    for item in counts:
        if current_facet != item["facet"]:
            current_facet = item["facet"]
            facet_lines.extend([f"## {current_facet}", ""])
        facet_lines.append(f"- `{item['value']}`: {item['count']} note(s)")
    if not counts:
        facet_lines.append("No tags recorded.")
    facet_lines.append("")

    question_lines = [GENERATED_NOTICE, "# Open Questions And Negative Knowledge", "", "## Open Questions", ""]
    open_docs = [doc for doc in docs if doc["kind"] == "open-question"]
    if not open_docs:
        question_lines.append("No open-question notes recorded.")
    for doc in open_docs:
        question_lines.append(format_doc_line(doc))
        question_lines.append(f"  Summary: {doc['summary']}")
    question_lines.extend(["", "## Negative Results", ""])
    negative_docs = [doc for doc in docs if doc["kind"] == "negative-result"]
    if not negative_docs:
        question_lines.append("No negative-result notes recorded.")
    for doc in negative_docs:
        question_lines.append(format_doc_line(doc))
        question_lines.append(f"  Summary: {doc['summary']}")

    graph_lines = ["graph TD"]
    if not docs:
        graph_lines.append('  empty["No corpus notes"]')
    for doc in docs:
        node = doc["doc_id"].replace("-", "_")
        label = f"{doc['doc_id']}: {doc['title']}".replace('"', "'")
        graph_lines.append(f'  {node}["{label}"]')
    for edge in edges:
        src = edge["src_doc_id"].replace("-", "_")
        dst = edge["dst_doc_id"].replace("-", "_")
        pred = edge["predicate"].replace('"', "'")
        graph_lines.append(f"  {src} -->|{pred}| {dst}")

    return {
        "index.md": "\n".join(index_lines).rstrip() + "\n",
        "facets.md": "\n".join(facet_lines).rstrip() + "\n",
        "open-questions.md": "\n".join(question_lines).rstrip() + "\n",
        "graph.mmd": "\n".join(graph_lines).rstrip() + "\n",
    }


